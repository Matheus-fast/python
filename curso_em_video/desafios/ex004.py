n = input('Digite algo: ')
print('O tipo primitivo desse valor é \033[36m', type(n), '\033[m')
print('Só tem espaços? \033[31m', n.isspace(), '\033[m')
print('É um número? \033[32m', n.isnumeric(), '\033[m')
print('É alfabético? \033[31m', n.isalpha(), '\033[m')
print('É alfanúmerico? \033[32m', n.isalnum(), '\033[m')
print('Está em MAIÚSCULAS? \033[31m', n.isupper(), '\033[m')
print('Está em minúsculas? \033[32m', n.islower(), '\033[m')
print('Está capitalizada? \033[31m', n.istitle(), '\033[m')